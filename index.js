const dayHours = 10
const dayStart = 9

const timeToDecimal = (t) => {
    let arr = t.split(':');
    let dec = parseInt((arr[1]/6)*10, 10);

    return parseFloat(parseInt(arr[0], 10) + '.' + (dec<10?'0':'') + dec);
}

const calculStartHour = (start_hours) => {
    return timeToDecimal(start_hours) - dayStart
}

const calculTaskTime = (start_hours, end_hours) => {
    return timeToDecimal(end_hours) - timeToDecimal(start_hours)
}

const getTaskDay = (start_hour_text) => {
    if(start_hour_text["Start Text"]) {
        const startDate = start_hour_text["Start Text"].split(',')[0]
        const startDay = startDate.split('/')[1]
        return startDay
    }
}

const convert = (timestamp) => {
    let h = new Date(Number(timestamp)).getHours();
	let m = new Date(Number(timestamp)).getMinutes();
	return `${h}:${m}`
}


for (let i = 0; i < 32; i++) {
    const timeline = document.querySelector(`.timeline-${i+1}`)
    const found = felix.filter(element => Number(getTaskDay(element)) == i);
    if(found.length > 0) {
		timeline.innerHTML = `<p class="timeline__day">Jour ${i}/11</p>`
        for (let j = 0; j < found.length; j++) {
            let taskTime = calculTaskTime(convert(found[j]["Start"]), convert(found[j]["Stop"]))
			if(taskTime > 0.1) {
				let width = (taskTime / dayHours) * 100
				let left = (calculStartHour(convert(found[j]["Start"])) / dayHours) * 100
				const taskHtml = `
				<div class="task">
					<div class="task__time" style="width: ${width}%; left:${left}%">
					</div>
					<p class="task__name" style="left:${left}%">
						${found[j]['Task Name']}
					</p>
				</div>
				`
				timeline.innerHTML += taskHtml
			}
        }
    } else {
		timeline.classList.add('d-none')
	}
}